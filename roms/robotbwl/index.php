					<h2>Description</h2>
					<p>
					<b>Robot Bowl</b> was a black &amp; white bowling alley game designed and programmed by Edward Valeau
					and Howell Ivey of Exidy. The game featured one or two robot bowlers playing with the standard bowling
					rules. To control the outcome, the game had five buttons: left, right, shoot, hook left, and hook right.
					"Hooking" the ball was the key to getting a good score, as you could only hook the ball after it had been
					thrown. This made it easy to pick up a spare, but the game made up for it by making splits very common.
					</p>

					<p>
					Robot Bowl was available in two different dedicated cabinets, an upright and a cocktail, both of
					them used the same internal hardware.
					</p>

					<p>
					The Robot Bowl upright was of the common 1970s 'short cabinet' design, as the machine had no
					marquee and was only as tall as the monitor. Some machines also had a decorative 'ball return'
					on the front at the very bottom of the cabinet. There is an interesting story behind that.
					At the time the game was created, Exidy had just purchased Fun Games and had a number of cabinets
					left over from one of Fun's driving games. They used these cabinets to get Robot Bowl into production
					quickly. The only problem was that the cabinets had a hole in the front where the gas pedal had been.
					Michael Cooper-Hart suggested that they silk-screen the words 'Ball Return' over the hole.
					</p>

					<p>
					The Robot Bowl cocktail was a square box with a perfectly round top. The player controls were on
					either side of the monitor (and you actually had to look at the game sideways to play). This machine
					had no decoration of any kind.
					</p>


